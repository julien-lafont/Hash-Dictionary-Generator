#ifndef COLORS_H
#define	COLORS_H

/*
 * Gestion des couleurs
 */

// Reset
#define COLOR_OFF "\e[0m"

// Couleur basique
#define COLOR_BLACK "\e[0;30m"
#define COLOR_RED "\e[0;31m"
#define COLOR_GREEN "\e[0;32m"
#define COLOR_YELLOW "\e[0;33m"
#define COLOR_BLUE "\e[0;34m"
#define COLOR_PURPLE "\e[0;35m"
#define COLOR_CYAN "\e[0;36m"
#define COLOR_WHITE "\e[0;37m"


// Couleurs gras
#define COLOR_GRAS_BLACK "\e[1;30m"
#define COLOR_GRAS_RED "\e[1;31m"
#define COLOR_GRAS_GREEN "\e[1;32m"
#define COLOR_GRAS_YELLOW "\e[1;33m"
#define COLOR_GRAS_BLUE "\e[1;34m"
#define COLOR_GRAS_PURPLE "\e[1;35m"
#define COLOR_GRAS_CYAN "\e[1;36m"
#define COLOR_GRAS_WHITE "\e[1;37m"

// Background
#define BG_BLACK "\e[40m"
#define BG_RED "\e[41m"
#define BG_GREEN "\e[42m"
#define BG_YELLOW "\e[43m"
#define BG_BLUE "\e[44m"
#define BG_PURPLE "\e[45m"
#define BG_CYAN "\e[46m"
#define BG_WHITE "\e[47m"

// High Intensity
#define COLOR_HIGH_BLACK "\e[0;90m"
#define COLOR_HIGH_RED "\e[0;91m"
#define COLOR_HIGH_GREEN "\e[0;92m"
#define COLOR_HIGH_YELLOW "\e[0;93m"
#define COLOR_HIGH_BLUE "\e[0;94m"
#define COLOR_HIGH_PURPLE "\e[0;95m"
#define COLOR_HIGH_CYAN "\e[0;96m"
#define COLOR_HIGH_WHITE "\e[0;97m"

// BG High Intensity
#define BG_HIGH_BLACK "\e[0;100m"
#define BG_HIGH_RED "\e[0;101m"
#define BG_HIGH_GREEN "\e[0;102m"
#define BG_HIGH_YELLOW "\e[0;130m"
#define BG_HIGH_BLUE "\e[0;104m"
#define BG_HIGH_PURPLE "\e[0;105m"
#define BG_HIGH_CYAN "\e[0;106m"
#define BG_HIGH_WHITE "\e[0;107m"

#endif	/* COLORS_H */

